package com.hlg.webgleaner.core.listener.bo;

/**
 * 集群机器地址。
 * 
 * @author yangwq
 * @Date 2016年6月1日
 */
public class ClusterAddress {
	
	private String host;
	
	private String port;

	public final String getHost() {
		return host;
	}

	public final void setHost(String host) {
		this.host = host;
	}

	public final String getPort() {
		return port;
	}

	public final void setPort(String port) {
		this.port = port;
	}
	
	
	
	
}
